package programs;

public class SecondProgram {

	public static void main(String[] args) {
		
		System.out.println(123); //integer value
		
		System.out.println('A'); //single quotes - character or char
		
		System.out.println("Ivan"); //double quotes - String
		
		System.out.println(12.309); //decimal
		
		System.out.println("Ivan+10+20+30");
		
		System.out.println("Ivan"+10+20+30); //+ concatenation
		
		System.out.println(10+20+30+"Ivan"+10+20+30);
	}

}
